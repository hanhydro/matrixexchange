#!/usr/bin/env python3
"""Build a compact main-text benchmark summary table (6 columns).

"""
from pathlib import Path

import numpy as np
import pandas as pd

PKG = Path(__file__).resolve().parent.parent
TABLES = PKG / "tables"
OUT_TEX = PKG / "manuscript" / "tables_tex"
OUT_TEX.mkdir(parents=True, exist_ok=True)


SITES = [
    ("USGS-07014500", "Meramec"),
    ("USGS-07067500", "Big Spring"),
    ("USGS-02322500", "Ichetucknee"),
    ("USGS-08155500", "Barton"),
    ("USGS-08169000", "Comal"),
    ("USGS-08171000", "Blanco"),
    ("11148900",      "Arroyo Seco"),
    ("1013500",       "Fish River"),
]

REGIME_DISPLAY = {
    "ridge": "ridge",
    "floor": "low-$\\mathrm{Da}$ floor",
    "simple_off_ridge": "low-$R$ off-shoulder ref.",
    "unclassified": "unclassified",
}


hi_env = pd.read_csv(TABLES / "benchmark_hi_envelope.csv")
regime = pd.read_csv(TABLES / "benchmark_regime_consistency.csv")
events = pd.read_csv(TABLES / "benchmark_event_summary.csv")
envelopes = pd.read_csv(TABLES / "benchmark_regime_envelopes.csv")


def _fmt_id(sid):
    s = str(sid).replace("USGS-", "")
    if s.isdigit():
        s = s.zfill(8)
    return s


body_rows = []
body_rows_si_full = []
for sid, label in SITES:
    hi_row = hi_env[hi_env["system_id"].astype(str) == sid]
    reg_rows = regime[regime["system_id"].astype(str) == sid]
    if hi_row.empty or reg_rows.empty:
        continue

    n = int(hi_row["n_events_baseline_preferred"].iloc[0])
    med = float(hi_row["HI_obs_median"].iloc[0])
    ci_lo = float(hi_row["HI_obs_median_ci_lo"].iloc[0])
    ci_hi = float(hi_row["HI_obs_median_ci_hi"].iloc[0])

    def _ov(reg):
        row = reg_rows[reg_rows["regime_id"] == reg]
        return float(row["overlap_fraction_obs"].iloc[0]) if not row.empty else 0.0

    ridge_ov = _ov("ridge")
    floor_ov = _ov("floor")
    ref_ov   = _ov("simple_off_ridge")

    # Event-fraction inside the ridge envelope (per-event HI membership)
    site_events = events[
        (events["system_id"].astype(str) == sid)
        & (events["filter_id"] == "baseline")
        & (events["data_resolution"] == "instantaneous")
        & events["HI_obs"].notna()
    ]
    hi_evt = site_events["HI_obs"].to_numpy(dtype=float)
    env_ridge = envelopes[(envelopes["system_id"].astype(str) == sid)
                          & (envelopes["regime_id"] == "ridge")]
    if not env_ridge.empty and len(hi_evt) > 0:
        rlo, rhi = float(env_ridge["HI_lo"].iloc[0]), float(env_ridge["HI_hi"].iloc[0])
        event_ridge_frac = float(((hi_evt >= rlo) & (hi_evt <= rhi)).mean())
    else:
        event_ridge_frac = float("nan")

    pref = str(reg_rows["preferred_regime_by_rule"].iloc[0])
    rule = str(reg_rows["preference_rule_applied"].iloc[0])
    disp = REGIME_DISPLAY.get(pref, pref).replace("_", r"\_")

    # Decision-basis shorthand for reader; use math-mode '<' so AGU cmr
    # font does not alias '<' to the inverted-exclamation glyph '¡'.
    rule_display = {
        "overlap_first": "overlap",
        "distance_when_overlap_nondiscriminating": "median-dist tie",
        "no_overlap": "$<$5\\% overlap",
    }.get(rule, rule.replace("_", r"\_"))

    evt_frac_str = f"{event_ridge_frac:.2f}" if np.isfinite(event_ridge_frac) else "--"
    # Main-text row: trimmed (6 columns per reviewer request)
    body_rows.append(
        " & ".join([
            f"{label} ({_fmt_id(sid)})",
            f"{n}",
            f"{med:.3f}",
            f"[{ci_lo:.3f}, {ci_hi:.3f}]",
            disp,
            rule_display,
        ]) + r" \\"
    )
    # SI audit row: full (overlaps + event ridge fraction)
    body_rows_si_full.append(
        " & ".join([
            f"{label} ({_fmt_id(sid)})",
            f"{n}",
            f"{med:.3f}",
            f"[{ci_lo:.3f}, {ci_hi:.3f}]",
            f"{ridge_ov:.4f}",
            f"{floor_ov:.4f}",
            f"{ref_ov:.4f}",
            evt_frac_str,
            disp,
            rule_display,
        ]) + r" \\"
    )


tex_main = (
    "% Auto-generated by regen_main_benchmark_summary.py — do not edit.\n"
    "\\begin{table}[ht]\n"
    "\\caption{Benchmark headline summary (compact main-text version). "
    "Reports, for each site: event count $n_{\\mathrm{evt}}$; site HI "
    "median under the baseline filter at preferred resolution; the "
    "\\textit{classifier interval} (bootstrap 10--90 CI of the site HI "
    "median used by the rule-based decision rule); the rule-based "
    "\\textit{preferred regime}; and the decision-rule \\textit{basis} "
    "(overlap-first, median-distance tie-break, or $<5\\%$ overlap "
    "$\\rightarrow$ \\textit{unclassified}). The full decision audit "
    "--- overlap fractions against each envelope, strict ridge-containment "
    "fraction at the event level, and per-site envelope bounds --- is "
    "in SI Table~15 (full audit) and SI"
    "Table~14 (per-site envelope comparison). The low-$R$ off-shoulder "
    "reference envelope is $[\\min, \\max]$-anchored (\\textit{broad, "
    "extremum-anchored, and classifier-dominant by design}); the "
    "$[q_{05}, q_{95}]$ quantile-bounded sensitivity rerun is in SI "
    "Table~23.}\n"
    "\\label{tab:benchmark_summary}\n"
    "\\centering\n"
    "\\small\n"
    "\\begin{tabular}{lrrcll}\n"
    "\\toprule\n"
    "Site & $n_{\\mathrm{evt}}$ & HI med. & Classifier CI & Preferred regime & Basis \\\\\n"
    "\\midrule\n"
    + "\n".join(body_rows) +
    "\n\\bottomrule\n\\end{tabular}\n\\end{table}\n"
)
(OUT_TEX / "benchmark_summary_main.tex").write_text(tex_main)

tex_si = (
    "% Auto-generated by regen_main_benchmark_summary.py — do not edit.\n"
    "\\begin{table}[ht]\n"
    "\\caption{Benchmark full audit table (SI companion to main-text "
    "main-text Table~2). Adds the four decision columns "
    "not shown in the compact main-text version: overlap fractions of the "
    "classifier interval against the ridge, low-$\\mathrm{Da}$ floor, and "
    "low-$R$ off-shoulder reference envelopes at the site's "
    "literature-constrained $R$ prior; and the \\textit{strict "
    "ridge-containment fraction} (fraction of baseline-filter events "
    "whose individual HI satisfies $\\mathrm{HI}_{\\mathrm{event}} \\in "
    "[q_{10}, q_{90}]$ of the ridge envelope). The Barton narrative "
    "``mixed case'' label uses the looser \\textit{below-ridge-upper-bound} "
    "counting rule ($10/15 = 0.67$), not this strict containment column "
    "(Barton strict = $0.13$). Preferred regime is the envelope with the "
    "largest overlap, except when one envelope fully contains the "
    "classifier interval and at least one other covers $\\geq 50\\%$ of "
    "it, in which case the median-distance tie-breaker applies. Overlap "
    "fractions against the low-$R$ off-shoulder reference use the "
    "production $[\\min, \\max]$-anchored envelope; the $[q_{05}, q_{95}]$ "
    "sensitivity is in Table~23.}\n"
    "\\label{tab:benchmark_summary_full}\n"
    "\\centering\n"
    "\\scriptsize\n"
    "\\begin{tabular}{lrrcrrrrll}\n"
    "\\toprule\n"
    "Site & $n_{\\mathrm{evt}}$ & HI med. & Classifier CI & Ridge ov. & Floor ov. & Ref. ov. & Strict ridge frac. & Preferred regime & Basis \\\\\n"
    "\\midrule\n"
    + "\n".join(body_rows_si_full) +
    "\n\\bottomrule\n\\end{tabular}\n\\end{table}\n"
)
(OUT_TEX / "benchmark_summary_si_full.tex").write_text(tex_si)

print(f"Wrote manuscript/tables_tex/benchmark_summary_main.tex "
      f"({len(body_rows)} rows, compact main-text version)")
print(f"Wrote manuscript/tables_tex/benchmark_summary_si_full.tex "
      f"({len(body_rows_si_full)} rows, SI full-audit version)")
