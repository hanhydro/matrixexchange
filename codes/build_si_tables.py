def _fmt(x, prec=3):
    """LaTeX-safe scalar formatter."""
    if x is None or (isinstance(x, float) and np.isnan(x)):
        return "---"
    if isinstance(x, (int, np.integer)):
        return f"{x:,}"
    if isinstance(x, float):
        if x == 0:
            return "0"
        ax = abs(x)
        if ax < 1e-3 or ax >= 1e4:
            return f"${x:.{prec - 1}e}$".replace("e", r" \times 10^{") + "}$"
        return f"{x:.{prec}g}"
    s = str(x).replace("_", r"\_").replace("%", r"\%").replace("&", r"\&")
    return s


def write_table(name, caption, label, header, rows, colspec=None, fontsize=r"\small"):
    """Emit a `\begin{table}` block with `tabular` body."""
    if colspec is None:
        colspec = "l" + "r" * (len(header) - 1)
    head_tex = " & ".join(header) + r" \\"
    body = "\n".join(" & ".join(r) + r" \\" for r in rows)
    tex = (
        f"% Auto-generated by build_si_tables.py — do not edit.\n"
        f"\\begin{{table}}[ht]\n"
        f"\\caption{{{caption}}}\n"
        f"\\label{{{label}}}\n"
        f"\\centering\n"
        f"{fontsize}\n"
        f"\\begin{{tabular}}{{{colspec}}}\n"
        f"\\toprule\n"
        f"{head_tex}\n"
        f"\\midrule\n"
        f"{body}\n"
        f"\\bottomrule\n"
        f"\\end{{tabular}}\n"
        f"\\end{{table}}\n"
    )
    out = TABLES_TEX / f"{name}.tex"
    out.write_text(tex)
    print(f"  wrote {out.relative_to(PKG)} ({len(rows)} rows)")


