def _sci(x, prec=2):
    """Format x as $a \\times 10^{b}$ for LaTeX."""
    if x == 0 or not np.isfinite(x):
        return "0"
    exp = int(np.floor(np.log10(abs(x))))
    mant = x / (10 ** exp)
    return f"${mant:.{prec}f}\\times 10^{{{exp}}}$"


def _display_id(sid: str) -> str:
    """Format a system_id for display in SI tables.

    USGS gauge IDs are canonically 8 digits; some of our internal CSV
    fields store them without the leading zero (e.g., "1013500" for Fish
    River near Fort Kent, ME). For display we strip the "USGS-" prefix
    and zero-pad pure-numeric IDs to 8 digits so the shown identifier
    matches USGS NWIS and the main text.
    """
    s = str(sid).replace("USGS-", "")
    if s.isdigit():
        s = s.zfill(8)
    return s.replace("_", r"\_")


def t_dt_convergence():
    df = pd.read_csv(TABLES_CSV / "dt_convergence.csv")
